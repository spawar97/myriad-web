
export const initialState = {
  isLoading: false,
  isError: false,
  categoryData: [],
}

const RactAssessmentFetchReducer = (state = initialState, action) => {
  switch (action.type) {
    case 'FETCH_INIT':
      return {
        ...state,
        isLoading: true,
        isError: false
      };
    case 'FETCH_SUCCESS':
      return {
        ...state,
        isLoading: false,
        isError: false,
        categoryData: action.payload,
      };
    case 'FETCH_FAILURE':
      return {
        ...state,
        isLoading: false,
        isError: true,
      };
    default:
      throw new Error();
  }
};

export default RactAssessmentFetchReducer;
