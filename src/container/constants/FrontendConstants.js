// Helper humanizer function.
function humanizer(millis) {
  if (millis === 24 * 60 * 60 * 1000) {
    // FIXME: We specify 24 hours here as a visual preference -- this value would result in "a day"
    // in the default case. `Moment.duration(millis).humanize()` also does weird rounding and is not
    // always exact, so this should be revisited if people start to use timeout values that are not "nice",
    // e.g. 26 days parses as "a month".
    return '24 hours';
  } else if (millis === 2 * 24 * 60 * 60 * 1000) {
    return '48 hours';
  } else {
    return Moment.duration(millis).humanize();
  }
}

function convertReportToAnalytics(fileType) {
  if (fileType === ExposureAppConstants.FILE_TYPE_REPORT) {
    fileType = ExposureAppConstants.FILE_TYPE_ANALYTICS;
  }
  return fileType;
}


